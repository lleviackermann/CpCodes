long long binpow(long long a, long long b, long long m) {
    a %= m;
    long long res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll n, mod;
    cin >> n >> mod;
    vl fact(3*n+1, 1), inv(3*n + 1, 1);
    for(int i = 2; i <= 3*n; i++) fact[i] = fact[i-1] * i % mod;
    inv[3*n] = binpow(fact[3*n], mod-2, mod);
    for(int i = 3*n-1; i >= 0; i--) inv[i] = inv[i+1] * (i+1) % mod;
    auto nCr = [&](ll n, ll r) -> ll {
        assert(n >= r);
        ll ans = fact[n] * inv[r] % mod;
        return ans * inv[n-r] % mod;
    };
    // case 1
    ll one_count = 2ll * fact[2*n] % mod;
    one_count = (one_count - 1 - fact[n] + mod) % mod;
    ll three_count = 0;
    ll two_count = nCr(2*n, n) * fact[n] % mod;
    two_count = two_count * fact[2*n] % mod;
    two_count = two_count * 2 % mod;
    debug(two_count);
    ll common = 0;
    for(ll i = 0; i <= n; i++) {
        ll x = nCr(n, i) * nCr(n, n-i) % mod;
        x = (x * nCr(2*n-i, n) % mod) * (fact[n] * fact[n] % mod) % mod;
        x = x * fact[n] % mod;
        debug2(i, x);
        common = common + x;
        common = common % mod;
    }
    two_count = (two_count - common + mod) % mod;
    two_count = (two_count - ((one_count + 1) % mod) + mod) % mod;
    three_count = (fact[3*n] - ((one_count + two_count + 1) % mod) + mod) % mod;
    debug3(one_count, two_count, three_count);
    ll ans = one_count + two_count * 2 + three_count * 3;
    cout << ans % mod << endl;    
}

