void solve()
{
    int n;
    // cin>>n;
    string s, t;
    cin>>s>>t;
    n = s.length();
    int time, q;
    cin>>time>>q;
    sl matched, unmatched;
    for(int i = 0; i < n; i++) {
        if(s[i]!=t[i]) unmatched.insert(i);
        else matched.insert(i);
    }

    vi unblockTime(q + time + 10, -1);
    int temp = 1;
    while(q--) {
        int flag;
        cin>>flag;
        if(unblockTime[temp]!=-1) {
            int x = unblockTime[temp];
            x--;
            if(s[x]!=t[x]) unmatched.insert(x);
            else matched.insert(x);
        }
        if(flag == 3) {
            if(unmatched.empty()) cout<<"YES\n";
            else cout<<"NO\n";
        } else if(flag == 2) {
            int x, pos1, y, pos2;
            cin>>x>>pos1>>y>>pos2;
            pos1--,pos2--;
            if(x==y) {
                if(x == 1) swap(s[pos1], s[pos2]);
                else swap(t[pos1], t[pos2]);
            } else {
                if(x == 1) swap(s[pos1], t[pos2]);
                else swap(s[pos2], t[pos1]);
            }
            if(s[pos1]!=t[pos1]) unmatched.insert(pos1), matched.erase(pos1);
            else matched.insert(pos1), unmatched.erase(pos1);
            if(s[pos2]!=t[pos2]) unmatched.insert(pos2), matched.erase(pos2);
            else matched.insert(pos2), unmatched.erase(pos2);
        } else {
            int pos;
            cin>>pos;
            unblockTime[temp + time] = pos;
            matched.erase(pos-1);
            unmatched.erase(pos-1);
        }
        temp++;
    }
}

